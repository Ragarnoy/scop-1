#include "libmat4.h"

void	mat4_transpose(float *m)
{
	int		h;
	int		w;
	float	t[16];

	h = -1;
	while (++h < 4)
	{
		w = -1;
		while (++w < 4)
			t[w * 4 + h] = m[h * 4 + w];
	}
	mat4_copy(m, t);
}
