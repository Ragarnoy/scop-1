#include "libmat4.h"

void	mat4_print(t_mat4 *m)
{
	int h;
	int	w;

	h = 0;
	while (h < 16)
	{
		printf("[ ");
		w = -1;
		while (++w < 4)
			printf("%.3f ", m->m[h + w]);
		printf("]\n");
		h += 4;
	}
	printf("\n");
}
