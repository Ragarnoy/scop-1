#include "libmat4.h"

void	mat4_mul(float *a, float *b)
{
	int		h;
	int		w;
	int		z;
	float	m[16];

	h = -1;
	while (++h < 4)
	{
		w = -1;
		while (++w < 4)
		{
			z = -1;
			m[h * 4 + w] = 0;
			while (++z < 4)
				m[h * 4 + w] += a[h * 4 + z] * b[z * 4 + w];
		}
	}
	mat4_copy(a, m);
}
